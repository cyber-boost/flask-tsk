// Add entrance animations and interactive effects
document.addEventListener('DOMContentLoaded', function() {
    // Staggered entrance animations
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, { threshold: 0.1 });
    
    const animatedElements = document.querySelectorAll('.footer-brand-section, .footer-navigation-creative, .newsletter-creative, .social-creative');
    animatedElements.forEach((element, index) => {
        element.style.opacity = '0';
        element.style.transform = 'translateY(40px)';
        element.style.transition = `all 0.8s ease ${index * 0.2}s`;
        observer.observe(element);
    });
    
    // Interactive logo layers
    const logoLayers = document.querySelector('.logo-layers');
    if (logoLayers) {
        logoLayers.addEventListener('mouseenter', function() {
            this.style.transform = 'scale(1.1) rotate(5deg)';
        });
        
        logoLayers.addEventListener('mouseleave', function() {
            this.style.transform = 'scale(1) rotate(0deg)';
        });
    }
    
    // Social platform hover effects
    const socialPlatforms = document.querySelectorAll('.social-platform');
    socialPlatforms.forEach(platform => {
        platform.addEventListener('mouseenter', function() {
            const icon = this.querySelector('.platform-icon');
            icon.style.transform = 'scale(1.2) rotate(10deg)';
        });
        
        platform.addEventListener('mouseleave', function() {
            const icon = this.querySelector('.platform-icon');
            icon.style.transform = 'scale(1) rotate(0deg)';
        });
    });
    
    // Achievement items hover effects
    const achievementItems = document.querySelectorAll('.achievement-item');
    achievementItems.forEach(item => {
        item.addEventListener('mouseenter', function() {
            const icon = this.querySelector('.achievement-icon');
            icon.style.transform = 'scale(1.3) rotate(15deg)';
        });
        
        item.addEventListener('mouseleave', function() {
            const icon = this.querySelector('.achievement-icon');
            icon.style.transform = 'scale(1) rotate(0deg)';
        });
    });
    
    // Add particles effect to background
    createParticles();
});

function createParticles() {
    const particleContainer = document.createElement('div');
    particleContainer.style.cssText = `
        position: absolute;
        top: 0;
        left: 0;
        right: 0;
        bottom: 0;
        pointer-events: none;
        z-index: 1;
    `;
    
    document.querySelector('.footer-background').appendChild(particleContainer);
    
    for (let i = 0; i < 20; i++) {
        const particle = document.createElement('div');
        particle.style.cssText = `
            position: absolute;
            width: 2px;
            height: 2px;
            background: rgba(102, 126, 234, 0.3);
            border-radius: 50%;
            left: ${Math.random() * 100}%;
            top: ${Math.random() * 100}%;
            animation: particleFloat ${5 + Math.random() * 10}s linear infinite;
        `;
        particleContainer.appendChild(particle);
    }
    
    // Add particle animation keyframes
    const style = document.createElement('style');
    style.textContent = `
        @keyframes particleFloat {
            0% { transform: translateY(0) translateX(0); opacity: 0; }
            10% { opacity: 1; }
            90% { opacity: 1; }
            100% { transform: translateY(-100px) translateX(${Math.random() * 100 - 50}px); opacity: 0; }
        }
    `;
    document.head.appendChild(style);
}
